# metodos das listas:
# .append(x) -> adiciona item no final da lista
# .insert(indice, x) -> adiciona o item na posição do indice e passa pra frente os itens pospostos
# sum(lista) -> soma todos os termos da lista
# len(lista) -> tamanho da lista(quant itens)
# del(lista[indice]) -> remove da lista pelo indice
# .remove(x) -> remove o item pelo seu alor
# .pop(indice) -> remove o valor pelo indice, se não especificar remove o ultimo item
# .count(x) -> mostra a quantidade de x na lista
# .index(x, (a partir de indice)) -> mostra o index de x a partir de uma posição especiica(mostra a primeira aparição caso não especifique)
# .sort() -> ordena a lista de modo crescente 
# .sort(reverse=True) -> ordena de modo decrescente